function parseAdminIps(): Set<string> {
  const ips = new Set<string>();
  
  // Always allow localhost for development
  ips.add("127.0.0.1");
  ips.add("::1");
  ips.add("localhost");
  
  const envIps = process.env.ADMIN_WHITELIST_IPS || "";
  if (envIps) {
    envIps.split(",").forEach(ip => {
      const trimmed = ip.trim();
      if (trimmed) {
        // Normalize: remove protocol if present
        const normalized = trimmed
          .replace(/^https?:\/\//, '')
          .replace(/\/.*$/, '')
          .split(':')[0]; // Remove port
        ips.add(normalized);
        // Also add IPv6-mapped IPv4 representation
        if (normalized.includes('.')) {
          const parts = normalized.split('.');
          const ipv6Mapped = `::ffff:${parts[0]}.${parts[1]}.${parts[2]}.${parts[3]}`;
          ips.add(ipv6Mapped);
        }
      }
    });
  }
  
  return ips;
}

const ADMIN_IPS = parseAdminIps();

export function adminIpWhitelist(req: Request, res: Response, next: NextFunction) {
  // Always enforce IP whitelist, even in dev mode
  // Previously: dev mode skipped ALL checks — staging servers were wide open

  // Production mode: require IP whitelist env var
  if (process.env.NODE_ENV === "production" && ADMIN_IPS.size <= 3) {
    console.error("[SECURITY] FATAL: ADMIN_WHITELIST_IPS is not set in production! Admin access blocked.");
    res.status(403).json({ error: "Access denied." });
    return;
  }

  // Get client IP — trust Express 'trust proxy' setting instead of raw headers
  const clientIp = req.ip || "";

  // Check if IP is allowed
  let isAllowed = ADMIN_IPS.has(clientIp);

  if (!isAllowed) {
    console.warn(`[SECURITY] Admin access blocked: ${clientIp} at ${new Date().toISOString()}`);
    res.status(403).json({ error: "Access denied." });
    return;
  }

  next();
}

